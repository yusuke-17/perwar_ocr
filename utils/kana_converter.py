"""
歴史的仮名遣い → 現代仮名遣い 変換モジュール

戦前文書で使われている歴史的仮名遣い（旧仮名遣い）を
現代仮名遣いに変換する。

注意:
  - 文脈によって変換が異なるケースがあるため、100%正確ではない
  - 長い文字列から先にマッチさせることで誤変換を減らしている
"""

# ---------- 歴史的仮名遣い → 現代仮名遣い 変換辞書 ----------
# 戦前公文書で頻出するパターンを収録
# 長い文字列を先に定義し、短い文字列での誤マッチを防ぐ

# ワ行 → ア行 の変換（助詞以外）
# 「は」「へ」「を」は助詞として使う場合があるため自動変換しない
